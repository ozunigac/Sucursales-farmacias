    private void LlenarTable(String query){
        for (int i = (model_productos.getRowCount() - 1); i >= 0; i--) // limpiamos tabla
            model_productos.removeRow(i);
//        28ae5f6449
        TableRowSorter<TableModel> orden = new TableRowSorter<TableModel>((DefaultTableModel)tablaProductos.getModel()); // modelo para la tabla
        tablaProductos.setRowSorter(orden); // add el ordenamiento
        String [][] res=cliente.consultaSelect(query);
        // recorremos el resultado de la ocnsulta
        for(int j=1;j<res.length;j++){
            model_productos.addRow(res[j]); // add a el modelo de l atabla
        }
    }
