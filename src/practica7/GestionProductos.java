    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = tablaProductos.getSelectedRow();
        //comprobamos que este seleccionando un usuario en la tabla
        if(row==-1){
            JOptionPane.showMessageDialog(null, "Seleccione un registro.", "Error", ERROR_MESSAGE);
        }else{
            //obtenemos el id del usuario
            int clave=Integer.parseInt(tablaProductos.getValueAt(row, 0).toString());
            String[][] emp = cliente.consultaSelect("select * from productos where (idProducto="+clave+")");
            int i=Integer.parseInt(emp[1][0]);
            //hacemos un objeto tipo producto y lo mandamos a otra interfaz para llenar el formulario
            Producto producto = new Producto(convint(emp[1][0]),emp[1][1],convint(emp[1][2]),convint(emp[1][3]),convint(emp[1][4]),emp[1][5],convint(emp[1][6]));
            AltaProductos alta = new AltaProductos(cliente,2,producto,empleado);
            alta.setVisible(true);
            this.setVisible(false);
            this.dispose();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //comprobamos que este seleccionando un usuario
        int row = tablaProductos.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(null, "Seleccione un registro.", "Error", ERROR_MESSAGE);
        }else{
            //preguntamos si quiere eliminar el usuario
            if(JOptionPane.showConfirmDialog(null, "Eliminar producto?", "Confirmar",1)==0){
                //preguntamos si esta seguro de hacer la eliminacion e la tabla
                if(JOptionPane.showConfirmDialog(null, "Estas seguro de eliminar el producto?", "Confirmar",1)==0){
                    //obtenemos el id del usuario
                    int clave=Integer.parseInt(tablaProductos.getValueAt(row, 0).toString());
                    //mandamos la consulta para eliminarlo
                    String respuesta = cliente.consultaInsert("delete from productos where (idProducto="+clave+")");
                    if(respuesta.equalsIgnoreCase("Exito")){
                        //actualizamos la pantalla
                        actualizarPantalla();
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

